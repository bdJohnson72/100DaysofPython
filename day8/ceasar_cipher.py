direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
text = input("Type your message:\n").lower()
shift = int(input("Type the shift number:\n"))


def encrypt(plain_text, shift_amount):
    cipher_text = ''
    for letter in plain_text:
        start = alphabet.index(letter)
        shifted_index = start + shift_amount
        if shifted_index > len(alphabet) - 1:
            adjusted_index = shifted_index - len(alphabet)
            cipher_text += alphabet[adjusted_index]
        else:
            cipher_text += alphabet[start + shift_amount]
    return cipher_text


def decrypt(cipher_text, shift_amount):
    plain_text = ''
    for letter in cipher_text:
        start = alphabet.index(letter)
        shifted_index = start + shift_amount
        plain_text += alphabet[start - shift_amount]
    return plain_text


if direction.lower() == 'encode':
    result = encrypt(plain_text=text, shift_amount=shift)
    print(result)
else:
    result = decrypt(cipher_text=text, shift_amount=shift)
